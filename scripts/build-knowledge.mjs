// Builds src/lib/knowledge-data.ts from the /knowledge folder.
// Runs automatically before every `npm run build` (see "prebuild" script),
// so adding a .md/.txt file to /knowledge and deploying is all it takes
// to teach the AI assistant something new.
import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const knowledgeDir = path.join(root, "knowledge");
const outFile = path.join(root, "src", "lib", "knowledge-data.ts");

/** Split a document into per-section chunks on "## " sub-headings. */
function toChunks(fileName, raw) {
  const text = raw.replace(/\r\n/g, "\n").trim();
  if (!text) return [];

  const baseTitle =
    text.match(/^#\s+(.+)$/m)?.[1].trim() ||
    fileName.replace(/\.(md|txt)$/i, "").replace(/[-_]+/g, " ");

  // Body without the top-level "# title" line.
  const body = text.replace(/^#\s+.+$/m, "").trim();
  const sections = body.split(/^##\s+/m);

  const chunks = [];
  // sections[0] is any intro text before the first "## "
  if (sections[0].trim()) {
    chunks.push({ title: baseTitle, text: sections[0].trim() });
  }
  for (const section of sections.slice(1)) {
    const nl = section.indexOf("\n");
    const heading = (nl === -1 ? section : section.slice(0, nl)).trim();
    const sectionBody = (nl === -1 ? "" : section.slice(nl + 1)).trim();
    if (sectionBody) {
      chunks.push({ title: `${baseTitle} — ${heading}`, text: sectionBody });
    }
  }
  return chunks.map((c, i) => ({ id: `doc:${fileName}:${i}`, ...c }));
}

let files = [];
if (existsSync(knowledgeDir)) {
  files = readdirSync(knowledgeDir)
    .filter((f) => /\.(md|txt)$/i.test(f))
    .filter((f) => !f.startsWith("_") && f.toLowerCase() !== "readme.md")
    .sort();
}

const docs = files.flatMap((f) =>
  toChunks(f, readFileSync(path.join(knowledgeDir, f), "utf8"))
);

mkdirSync(path.dirname(outFile), { recursive: true });
writeFileSync(
  outFile,
  `// AUTO-GENERATED by scripts/build-knowledge.mjs — do not edit by hand.
// Source: the /knowledge folder. Regenerate with \`npm run knowledge\`.
export type FirmDoc = { id: string; title: string; text: string };

export const firmDocs: FirmDoc[] = ${JSON.stringify(docs, null, 2)};
`
);

console.log(`knowledge: ${files.length} file(s) → ${docs.length} chunk(s) → src/lib/knowledge-data.ts`);
